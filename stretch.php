					<h1>Look Once, Think Twice</h1>
					<div class="article-intro">
						<p>Student Health &amp; Wellness launched their new class “Look Once, Think Twice” in Fall 2012 and is specifically designed for students who are in the presence of alcohol, but not consuming alcohol.</p>
					</div>
					<div class="article-meta">
						<p>WORDS BY <em id="page-article-author">Caitlin Scott</em></p>
						<p>PHOTO BY <em>Ben Lewis</em></p>
					</div>
					<div class="article-share">

					</div>
					<div class="article-content">
						<p>“[Look Once, Think Twice] lets students know they’re not the only person who has made the choice of abstinence,” says Danielle McGraw, Substance Abuse Counselor at UI. “It helps to kind of tie them to other people and to other resources. It also helps to increase students’ confidence.”</p>
						<p>According to McGraw, a potentially high-risk situation Student Health staff often see occurs when a student invites friends over to their residence hall room to drink. The other roommate might not be comfortable with the situation, but they may be unsure of how to handle the situation.</p>
						<p>Students living in the residence halls who are found to be in the presence of alcohol but not drinking, such as a roommate or friend, will face disciplinary action along with those consuming alcohol. These bystanders can either pay a fine ranging from $200 – $700, or choose to participate in the Look Once, Think Twice program. Prior to Fall 2012, all students would take the alcohol education workshop eCHECKUP TO GO before coming to campus. Student Health & Wellness staff felt that the workshop was not resonating with program participants.</p>
						<p>“We felt that a lot of those students might not be consuming alcohol.” says McGraw. “Many of the students might have already made the choice to be abstinent, so [eCHECKUP TO GO] was missing key messages for them.”</p>
						<p>The Look Once, Think Twice program seeks to meet the needs of students who choose abstinence as well as students who choose to consume alcohol in a low-risk manner.</p>
						<h2>Bystander Intervention Techniques</h2>
						<p>Look Once, Think Twice has three main objectives. The first goal is to ensure that students are able to identify bystander intervention techniques, which are used widely across the country, according to McGraw. The idea is to train bystanders to take actions other than silence or inactivity. For example, a first-year student living in the residence halls might confront a roommate who decides to drink alcohol in the dorm, as opposed to keeping quiet about the situation.</p>
						<p>“They learn how to handle situations that they might feel uncomfortable with and how to make better choices when a situation arises,” says McGraw. “It’s refreshing for them to know that they are not the only person that doesn’t know what to what to do or how to say something,”</p>
						<div class="full-quote"><p>“It’s refreshing for them to know that they are not the only person that doesn’t know what to what to do or how to say something.”</p></div>
						<p>Out of the 30 students that took the Look Once, Think Twice post-assessment survey, 29 students were able to identify at least one bystander intervention technique. Additionally, students are 22% more likely to seek out a neutral third party when in a high-risk drinking situation, according to the survey.</p>
						<h2>Locate alcohol-free resources at UI</h2>
						<p>Look Once, Think Twice also aims to inform students of the alcohol-free resources available on campus. The objective is to ensure that students can name and locate at least one university resource for alcohol-free events. According to the post-assessment survey, 100% of the students were able to identify and locate a website for alcohol-free events, or a person that would know, such as an advisor or an Residence Hall Assistant.</p>
						<h2>Confidence</h2>
						<p>The final objective of Look Once, Think Twice, is to boost students’ confidence in their ability to address a situation when it does not align with their values. Participating students go through a list of values and they are asked to identify which values they align with. According to McGraw, this helps students to understand their actions, which likely tie into one of their core values.</p>
						<div class="full-image">
							<img src="images/full-open.jpg" alt="">
						</div>
						<p>“Sitting down and looking at values is something that a lot of students don’t really have the chance to do and I think it’s important to be able to do that,” says McGraw.</p>
						<p>Before taking the class, students reported an average confidence level in their ability to address situations that don’t align with their values at 3.04 out of 4 on a Likert scale. The Likert scale is a psychometric scale commonly used  in research that uses questionnaires. The scale is one of the most common methods of assessing responses in survey research. After taking Look Once, Think Twice, students reported a confidence level of 3.30, which constitutes a 26% increase in confidence.</p>
						<p>“We are not in the business of preaching abstinence to people. We just want to help students reduce any type of negative effects form their own drinking, and from someone else’s drinking,” says McGraw. “We want to do anything we can to increase success of students here at the university and we think that bystander techniques and harm reduction efforts really play a big role in these efforts.”</p>
