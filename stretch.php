					<h1>Midwest Outdoor Leadership Conference</h1>
					<div class="article-intro">
						<p>Student Health &amp; Wellness launched their new class “Look Once, Think Twice” in Fall 2012 and is specifically designed for students who are in the presence of alcohol, but not consuming alcohol.</p>
					</div>
					<div class="article-meta">
						<p>WORDS BY <em id="page-article-author">Caitlin Scott</em></p>
						<p>PHOTO BY <em>Ben Lewis</em></p>
					</div>
					<div class="article-content">
						<p>The University of Iowa hosted nearly 100 students at the second annual Midwest Outdoor Leadership Conference (MWOLC) on Feb. 22-24, 2013. Every year MWOLC invites students from college outdoor recreation programs across the Midwest to meet for one weekend in order to learn skills that will benefit their future careers in the outdoor recreation industry.</p>

						<p>According to David Patton, Assistant Director for Recreational Services and Outdoor Programs at UI, the MWOLC is modeled on the Association of Outdoor Recreation and Education (AORE) Conference.</p>

						<p>“I go to AORE every year, but it’s mainly people like me, the full-time person that runs the outdoor program, and it’s sort of limiting to students,” Patton says, referencing AORE’s high cost, week-long timeframe, and distant location.  “It’s great for [full-time staff] to do that and stay on the edge, but we need opportunities for the students.”</p>

						<p>Outdoor recreation professionals at Miami University in Oxford, Ohio pinpointed the growing need for a convention tailored to aspiring outdoor recreation leaders and set out to create the first-ever Midwest Outdoor Leadership Conference, which Ohio State University hosted in 2011.</p>

						<p>“Last year, I promised that I’d bring a van full of Iowa students out to Ohio State to support the conference and we did,” Patton says. “We had so much fun. The conference organizers saw our enthusiasm and they asked us to host it this year, so we did.”</p>

						<p>MWOLC featured breakout sessions with special emphasis on leadership development, program management, professional development, and skill development. Attendees also had the chance to relax and have fun with events like a bouldering competition, open climbing time, and a documentary viewing.</p>

						<div class="full-quote"><p>“We had so much fun. The conference organizers saw our enthusiasm and they asked us to host it this year, so we did.”</p></div>

						<p>In order to keep the conference accessible to all students, the MWOLC charged a conference registration fee of $50, a relatively small payment considering that professional conferences like AORE cost hundreds of dollars. Students were able to camp on the turf of the Hawkeye Tennis and Recreation Complex (HTRC) for free. Marmot outfitters sponsored a raffle and the keynote speaker, professional mountaineer Fabrizio Zangrilli. The well-known mountaineer gave students an in-depth look at what it’s like to be a leader in outdoor recreation. Zangrilli told students about his extensive list of outdoor experiences, including key accomplishments like climbing K2 and guiding groups of people through the Himalayas.</p>

						<p>Conference attendees enjoyed the opportunity to learn from seasoned outdoor leaders like Zangrilli, and were grateful for the ability to network with like-minded people.</p>

						<p>“It is so easy to meet and talk to new people because we’re all here for the same thing. It’s been really helpful,” says Courtney Sullivan, junior at Bowling Greens State University.</p>

						<p>Patton’s entire student staff at the Campus Recreation and Wellness Center participated in the conference. The students were able to waive  the registration fee in exchange for volunteering to work the conference.
						Patton was proud to host the 2013 conference at UI’s facilities, but his favorite part of the three-day weekend was sharing the world of outdoor recreation with students.</p>

						<p>“The highlight of the conference for me is seeing the younger folks. It opens their eyes a bit to how big outdoor recreation is. It’s not just students at the Iowa Rec Center working the climbing wall,” Patton says. “It’s great for my staff to see that there are other people in the world, and other universities that do the same sort of thing that we do.”</p>

						<p>Students that attended the conference are just as pleased with the event as Patton. Andrew McQuillen, a student at the University of Dayton, shared his comments on the value of the weekend to Facebook.</p>

						“The Midwest Outdoor Leadership Conference at the University of Iowa was amazing. I heard some incredible stories and met some incredible people, all who share an awesome passion for the outdoors. I’m 100% confident in my decision to pursue outdoor education and recreation as a career.”</p>

						<div class="full-image">
							<img src="images/excel-outdoorconf-int1.jpg" alt="">
						</div>

						<p>MWOLC Keynote Speaker Zangrilli also gave UI conference organizers positive feedback. Zangrilli published his thoughts to Facebook, saying  “I had a super fun time, met some really interesting and motivated people and got to sample some great nightlife in Iowa City as well.” The mountaineer also playfully added a note to the school who hosts next year’s conference.  “Good luck next year to the university that puts the event on – [a] tough act to follow for sure!”</p>
